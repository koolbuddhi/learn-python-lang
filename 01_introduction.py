# Welcome to Python! This file will teach you the basics of Python programming.

# Comments in Python
# Comments start with a '#' symbol and are ignored by Python
# They help explain what the code does

# Print statements
print("Hello, World!")  # This is your first Python program!

# Variables
# Variables are containers for storing data
name = "John"          # String variable
age = 25              # Integer variable
height = 5.9          # Float variable

print("\nWorking with variables:")
print("Name:", name)
print("Age:", age)
print("Height:", height)

# Basic Operations
# Python can perform mathematical operations
x = 10
y = 5

print("\nBasic Mathematics:")
print("Addition:", x + y)      # 15
print("Subtraction:", x - y)   # 5
print("Multiplication:", x * y) # 50
print("Division:", x / y)      # 2.0

# String Operations
first_name = "John"
last_name = "Doe"

# String concatenation
full_name = first_name + " " + last_name
print("\nString operations:")
print("Full name:", full_name)

# Input from user
print("\nLet's try user input:")
user_name = input("What is your name? ")
print("Hello,", user_name + "!")

